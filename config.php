async function executeCancelOrder() {
  if (!currentOrderId) return;

  const yesBtn = document.querySelector('.btn-yes-cancel');
  yesBtn.disabled = true;
  yesBtn.textContent = '⏳ Cancelling...';

  try {
    const res = await fetch(`${BACKEND_URL}/api/orders.php?id=${currentOrderId}`, { method: 'DELETE' });
    const data = await res.json();

    if (data.success) {
      // Show cancelled state
      document.getElementById('step-cancel').style.display = 'none';
      document.getElementById('step-cancelled').style.display = 'block';
      currentOrderId = null;
    } else {
      showToast('❌ ' + (data.error || 'Could not cancel order'), 'error');
      backToSuccess();
    }
  } catch (err) {
    showToast('❌ Server error. Please try again.', 'error');
    backToSuccess();
  }

  yesBtn.disabled = false;
  yesBtn.textContent = 'Yes, Cancel';
}

