function updateTotal() {
  const qty = parseInt(document.getElementById('qty').value) || 1;
  const price = 2199;
  const total = qty * price;
  document.getElementById('total-price').textContent =
    `Total: ₹${total.toLocaleString('en-IN')} (${qty} jar${qty > 1 ? 's' : ''} × ₹${price.toLocaleString('en-IN')})`;
}

// ─── SUBMIT ORDER → MongoDB ───
async function submitOrder() {
  const name    = document.getElementById('order-name').value.trim();
  const phone   = document.getElementById('order-phone').value.trim();
  const address = document.getElementById('order-address').value.trim();
  const quantity = document.getElementById('order-qty').value;
  const payment = document.getElementById('order-payment').value;
  const message = document.getElementById('order-msg').value.trim();

  if (!name || !phone || !address) {
    showToast('⚠️ Please fill Name, Phone & Address', 'error');
    return;
  }
  if (phone.replace(/\D/g, '').length < 7) {
    showToast('⚠️ Please enter a valid phone number', 'error');
    return;
  }

  const btn = document.getElementById('submit-btn');
  btn.disabled = true;
  btn.innerHTML = '⏳ Placing your order...';

  const totalAmount = quantity.split('–')[1]?.trim() || '';

  try {
    const res = await fetch(`${BACKEND_URL}/api/orders.php`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ name, phone, address, quantity, payment, message, totalAmount })
    });
    const data = await res.json();

    if (data.success) {
      currentOrderId = data.orderId;
      currentOrderData = { name, phone, address, quantity, payment, totalAmount };
      showOrderConfirmation(currentOrderData, currentOrderId);

      // Clear form
      document.getElementById('order-name').value = '';
      document.getElementById('order-phone').value = '';
      document.getElementById('order-address').value = '';
      document.getElementById('order-msg').value = '';
    } else {
      showToast('❌ Error: ' + (data.error || 'Could not place order'), 'error');
    }
  } catch (err) {
    showToast('❌ Cannot connect to server. Please try again.', 'error');
  }

  btn.disabled = false;
  btn.innerHTML = '📦 Place Order';
}

